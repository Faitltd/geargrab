<script lang="ts">
  export let message: string;
  export let type: 'error' | 'warning' | 'info' = 'error';
  export let dismissible: boolean = true;
  export let onDismiss: (() => void) | undefined = undefined;
  
  let visible = true;
  
  const handleDismiss = () => {
    visible = false;
    if (onDismiss) {
      onDismiss();
    }
  };
  
  const typeStyles = {
    error: {
      bg: 'bg-red-50 border-red-200',
      text: 'text-red-800',
      icon: 'text-red-500',
      accent: 'text-accent-500'
    },
    warning: {
      bg: 'bg-accent-50 border-accent-200',
      text: 'text-accent-800',
      icon: 'text-accent-500',
      accent: 'text-primary-500'
    },
    info: {
      bg: 'bg-primary-50 border-primary-200',
      text: 'text-primary-800',
      icon: 'text-primary-500',
      accent: 'text-accent-500'
    }
  };
  
  $: styles = typeStyles[type];
</script>

{#if visible}
  <div class="rounded-lg border-2 p-4 {styles.bg} {styles.text} animate-slide-down" role="alert">
    <div class="flex items-start">
      <!-- Icon -->
      <div class="flex-shrink-0">
        {#if type === 'error'}
          <svg class="w-5 h-5 {styles.icon}" fill="currentColor" viewBox="0 0 20 20">
            <path fill-rule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z" clip-rule="evenodd" />
          </svg>
        {:else if type === 'warning'}
          <svg class="w-5 h-5 {styles.icon}" fill="currentColor" viewBox="0 0 20 20">
            <path fill-rule="evenodd" d="M8.257 3.099c.765-1.36 2.722-1.36 3.486 0l5.58 9.92c.75 1.334-.213 2.98-1.742 2.98H4.42c-1.53 0-2.493-1.646-1.743-2.98l5.58-9.92zM11 13a1 1 0 11-2 0 1 1 0 012 0zm-1-8a1 1 0 00-1 1v3a1 1 0 002 0V6a1 1 0 00-1-1z" clip-rule="evenodd" />
          </svg>
        {:else}
          <svg class="w-5 h-5 {styles.icon}" fill="currentColor" viewBox="0 0 20 20">
            <path fill-rule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z" clip-rule="evenodd" />
          </svg>
        {/if}
      </div>
      
      <!-- Message -->
      <div class="ml-3 flex-1">
        <p class="text-sm font-medium">
          {message}
        </p>
      </div>
      
      <!-- Dismiss button -->
      {#if dismissible}
        <div class="ml-auto pl-3">
          <button
            type="button"
            class="inline-flex rounded-md p-1.5 {styles.text} hover:{styles.accent} focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-{type === 'error' ? 'red' : type === 'warning' ? 'accent' : 'primary'}-500 transition-colors"
            on:click={handleDismiss}
            aria-label="Dismiss"
          >
            <svg class="w-4 h-4" fill="currentColor" viewBox="0 0 20 20">
              <path fill-rule="evenodd" d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z" clip-rule="evenodd" />
            </svg>
          </button>
        </div>
      {/if}
    </div>
  </div>
{/if}

<style>
  @keyframes slide-down {
    from {
      opacity: 0;
      transform: translateY(-10px);
    }
    to {
      opacity: 1;
      transform: translateY(0);
    }
  }
  
  .animate-slide-down {
    animation: slide-down 0.3s ease-out;
  }
</style>
